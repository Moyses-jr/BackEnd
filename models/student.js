const { model, Schema } = require("mongoose");

const studentShema = new Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const student = model("student", studentShema);

module.exports = { student };
